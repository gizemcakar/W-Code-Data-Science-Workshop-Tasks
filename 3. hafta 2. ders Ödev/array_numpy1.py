# 1 - Creating a NumPy Array and Exploring its Properties
import numpy as np

array = np.array([1,2,3,4,5,6,7])
print(array)

# type of the array
print(type(array))

# number of dimensions of the array
print(array.ndim) 

# number of elements in the array
print(array.size)

# shape of the array(number of elements in each dimension)
print(array.shape)

